CREATE TABLE IF NOT EXISTS FAO_MAIN_ORDERS (
    ORDER_ID VARCHAR(40) NOT NULL,
    CLIENT_ID VARCHAR(40) NOT NULL,
    CLIENT_NAME VARCHAR(200),
    CLIENT_TYPE VARCHAR(40),
    SECURITY_ID VARCHAR(40) NOT NULL,
    SECURITY_NAME VARCHAR(200),
    SECURITY_TYPE VARCHAR(40),
    EXCHANGE VARCHAR(20),
    TRADE_DATE DATE NOT NULL,
    MONTH INT,
    QUARTER INT,
    YEAR INT,
    ORDER_TYPE VARCHAR(20),
    BUY_SELL_IND VARCHAR(4),
    QUANTITY DECIMAL(18, 4),
    PRICE DECIMAL(18, 6),
    GROSS_AMOUNT DECIMAL(20, 4),
    STANDARD_STATUS VARCHAR(20),
    ORDER_RISK_BUCKET VARCHAR(20),
    MAIN_LOAD_TIMESTAMP TIMESTAMP
);

